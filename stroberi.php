    <h1>Kandungan Nutrisi Stroberi</h1>
</div>
<div class="entry-body">
<div class="media">
  <a class="media-left" href="#">
  </a>
  <div class="media-body">
    <p>
    Stroberi sangat populer untuk produksi dan konsumsi karena rasanya yang 
enak dan kaya akan nutrisi. Kandungan vitamin A, karoten dan diet serat pada 
buah stroberi bermanfaat untuk menjaga kesehatan mata, mengurangi rabun senja 
serta meningkatkan kesehatan pencernaan. Buah stroberi 
memiliki kandungan nutrisi serta antioksidan yang tinggi, sehingga dapat 
bermanfaat dalam memerangi kardiovaskular penyakit, kanker dan masalah 
kesehatan lainnya. Banyak sekali kandungan nutrisi dalam 
stroberi yang baik bagi tubuh, juga memiliki nilai jual yang tinggi karena menjadi 
buah yang biasa dikonsumsi harian. Namun, Stroberi mudah sekali terinfeksi 
berbagai jenis hama dan penyakit.
    </p>
