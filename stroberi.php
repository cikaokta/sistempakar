    <h4 class="media-heading"><b>Kutu Thrips</b></h4>
    <p>
    Kutu ini menyerang stroberi di awal musim panas maupun penghujan. Gejala 
    yang terjadi yaitu terdapat perubahan pada warna daun menjadi kecoklatan, 
    daun keriting, buah berwarna hijau kecoklatan. Pengendalian dapat dilakukan 
    rutin setiap minggu dengan melakukan penyemprotan insektisida berbahan 
    aktif Abamectin.
    </p>
  </div>
</div>  
