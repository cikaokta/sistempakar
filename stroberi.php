   <h2>Summary</h2> 
    <p>
    Selama ini sejumlah varietas stroberi dari Amerika, Eropa, dan Jepang telah dibudidayakan 
    oleh para petani di tanah air. Masing-masing varietas memiliki kelebihan dan kelemahan; 
    ada yang dapat tumbuh di daerah bersuhu tinggi, ada yang hanya dapat berbuah beberapa kali 
    dalam setahun, ada yang tidak tahan penyakit, dan lain-lain. Tetapi yang cukup mencolok adalah 
    varietas-varietas itu rata-rata menghasilkan buah berukuran kecil yang didominasi oleh rasa masam.
    Dalam buku ini Anda akan mempelajari A-Z budi daya stroberi, mulai dari asal-usul stroberi, 
    cara memperbanyak stolon, variasi media tanam, masa tanam yang tepat, perawatan dari hari ke hari 
    hingga masa panen setelah 60 hari penanaman.
    <br><br>
    Penulis buku ini adalah seorang petani muda berpengalaman yang telah terbukti berhasil 
    membuat kultivar stroberi yang menghasilkan buah primer stroberi berwarna merah cerah 
    berukuran lebih besar (50 gram) dengan rasa yang jauh lebih manis daripada yang umumnya 
    beredar di pasar (derajat kemanisan 20% brix pada alat ukur refraktometer). 
    Di sini ia berbagi pengetahuan dan pengalamannya membudidayakan stroberi agar hasil panen 
    Anda memuaskan. 
