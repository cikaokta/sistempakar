  <div class="media-body">
    <h4 class="media-heading"><b>Spider Mites</b></h4>
    <p>
    Sering ditemukan pada stroberi yang ditanam dalam greenhouse, hama ini 
    menyukai lingkungan panas dengan tingkat kelembaban rendah. Gejala yang 
    timbul yaitu daun menguning, pada pinggiran daun terdapat seperti sarang 
    laba-laba. Pengendalian dilakukan dengan rutin membasahi bawah daun 
    stroberi dengan sprayer bertekanan tinggi setiap tiga hari sekali, melakukan
    penyemprotan Akarisida berbahan aktif Piridaben atau penyemprotan 
    berbahan aktif Sulfur pada sore menjelang malam hari.
    </p>
  </div>
</div> 
<div class="media">
  <a class="media-left" href="#">
    <img src="powder.jpg" class="coeg" height="280px" width="400px">
  </a>
  <div class="media-body">
    <h4 class="media-heading"><b>Cendawan Powdery Mildew</b></h4>
    <p>
    Menyerang jenis stroberi yang memiliki buah manis dan dibudidayakan 
    dalam greenhouse dengan kelebaban rendah. Ciri dari cendawan ini yaitu 
    memiliki hifa atau bercak berwarna putih, dapat menyerang buah, daun, 
    batang. Jika sudah parah, menimbulkan bercak ungu pada daun. Pengendalian 
    secara kimiawi untuk hama ini yaitu dengan menyemprotkan fungisida 
    berbahan aktif Dritazol, Metoksi Aklirat atau Difenokonazol.
    </p>
  </div>
</div>
