    <h1>Varietas Stroberi</h1>
</div>
    <p>Stroberi merupakan tipe tanaman short day plant, dimana tanaman tersebut akan berbunga dan 
berbuah ketika mendapatkan sinar matahari dengan durasi singkat serta suhu yang 
dingin. Dikenal sebagai June bearing, karena berbunga ketika musim semi dan 
buah dapat dipetik pada awal musim panas yaitu bulan juni. Amerika 
mengutamakan stroberi dengan tipe day neutral, yang berarti proses pembuahan
tidak dipengaruhi oleh lamanya sinar matahari atau dengan kata lain dapat 
berbunga sepanjang musim pada suhu 8-32 derajat Celcius. Sedangkan di Negara 
Tropis, stroberi biasa dibudidayakan di dataran tinggi meskipun kini sudah 
banyak yang menanamnya di dataran rendah.</p> <br>
