    <h1>Tips Budidaya Stroberi</h1>
</div>
<div class="entry-body">
<div class="media">
  <a class="media-left" href="#">
  <img src="stbr2.jpg" height="300px" width="400px">
  </a>
  <div class="media-body">
    <h4 class="media-heading"><b>Perawatan Harian</b></h4>
  <p>
    1. Melakukan pengecekan secara berkala setiap hari<br>
    2. Upaya pencegahan resiko terkena hama dan penyakit dengan rutin melakukan penyemprotan insektisida dan fungisida seminggu sekali. <br>
    3. Budidaya di dalam greenhouse untuk meminimalisir resiko terserang hama dan penyakit.<br>
    4. Perhatikan kebutuhan nutrisi stroberi dengan melakukan monitoring nutrisi setiap hari. Gunakan AB Mix dengan pekatan 600 ppm untuk nutrisi lengkap.<br><br>
  </P>
  <h4 class="media-heading"><b>Bisakah budidaya stroberi di dataran rendah?</b></h4>
  <p>
    Bisa, namun perlu perhatian lebih agar buah bisa berkembang dengan baik.<br>
    1. Buat humadity atau lingkungan sejuk dengan sirkulasi udara yang baik dan dibawah naungan.<br>
    2. Siram tanaman dtroberi dengan air es dengan tujuan untuk memacu pembungaan, karena stroberi membutuhkan suhu dingin untuk berbunga.<br>
  </P>
