    <h4 class="media-heading"><b>Cendawan Botrytis</b></h4>
    <p>
    Terjadi ketika tanaman stroberi kekurangan cahaya matahari atau kelembaban 
    terlalu tinggi pada daun stroberi. Ciri dari cendawan ini yaitu buah 
    mengalami kebusukan berwarna coklat. Pencegahan dapat dilakukan dengan 
    menghindari daun terlalu basah, dan pengendalian kimiawi dilakukan dengan 
    penyemprotan kombinasi insektisida Amistar dan Score.
    </p>
