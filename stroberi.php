  <div class="media-body">
    <h4 class="media-heading"><b>Hama Kutu Daun (Aphid)</b></h4>
    <p>
    Hama aphid atau kutu kebul menyerang stroberi dengan menghisap sari daun 
    muda. Gejala yang terjadi yaitu timbul bercak serbuk kehitaman dan kondisi 
    daun pucuk kurang besar. Aphid dapat menjadi vektor virus sehingga harus 
    dikendalikan dengan rutin menyemprotkan insektisida berbahan aktif 
    Abamectin atau Profenofos secara berkala.
    </p>
