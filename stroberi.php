    <h1>Sejarah Stroberi</h1>
</div>
<p>
Tanaman stroberi termasuk kedalam famili Rosaceae dengan genus Fragaria, 
pertama kali dibudidayakan pada tahun 1300-an oleh bangsa Yunani dan Romawi 
sebagai tanaman hias di halaman istana kerajaan, jenis yang ditanam saat itu 
adalah Alphine Strawberry (Fragaria vesca. L).
</p>
<div class="entry-body">
<div class="media">
  <a class="media-left" href="#">
    <img src="asal.png" class="coeg" height="280px" width="500px">
  </a>
  <div class="media-body">
    <br>
    <p>
    Penduduk Amerika Utara membudidayakan stroberi asli negara tersebut 
berjenis Fragaria virginia, tanaman dengan kemampuan menahan suhu dingin 
dan kekeringan. Pada awal tahun 1600-an, Fragaria virginia dibawa ke Eropa. 
Tahun 1700-an, para penjelajah menemukan stroberi liar di hutan Chile dengan 
jenis Fragaria chiloensis, menghasilkan buah stroberi yang besar tetapi tidak 
cocok untuk berbagai iklim. Sejak tahun 1300-an, Eropa Utara termasuk Prancis 
membudidayakan stroberi hutan dengan jenis Fragaria vesca (L.). Pada tahun 
1714, Amédéé-Francois Frézier membawa tanaman stroberi dengan jenis F. 
chiloensis yang kemudian ditanam bersamaan dengan jenis F. virginiana di Brest, 
Perancis. Hybrid natural dari kedua jenis tersebut menghasilkan jenis baru dengan 
buah yang besar, dikenal dengan nama Fragaria x ananassa Duch yang menjadi 
sejarah awal mula budidaya stroberi modern.
    </p>
