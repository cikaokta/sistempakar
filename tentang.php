    <h4 class="media-heading"><b>Apa itu Sistem Pakar?</b></h4>
    <p>
Sistem pakar menjadi sarana untuk berbagi serta mendistribusikan 
pengetahuan, baik yang diperoleh secara langsung maupun tidak langsung dari 
pakar di bidang tertentu. Kekuatan sistem pakar terletak pada kemampuan sistem 
dalam meniru proses pengambilan keputusan layaknya pakar manusia. Banyak 
aplikasi telah dikembangkan untuk mendukung diagnosis berbagai penyakit 
disertai saran prosedur pengobatan dari diagnosis yang dialami pengguna, hal ini 
tentu membawa perbaikan pada proses diagnosis juga memudahkan pengobatan 
baik dari segi keandalan maupun efisiensi karena diagnosis dapat dilakukan sedini 
mungkin di rumah.
